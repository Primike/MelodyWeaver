import Foundation

struct Song: Codable, Identifiable, Equatable, Comparable {
    let id: UUID
    var name: String
    var time: Date
    var notes: [Int]
    var speed: [Int]
    
    var date: String {
        let formatter = DateFormatter()
        formatter.dateFormat = "MM/dd/yyyy"
        return formatter.string(from: time)
    }
    
    static func == (l: Song, r: Song) -> Bool {
        return l.id == r.id
    }
    
    static func < (l: Song, r: Song) -> Bool {
        return l.time == r.time
    }
}

